// It has three fixed values: CLASS_4, CLASS_6, and CLASS_10
// Each class represents a different speed category for a memory card

public enum CardClass {
    CLASS_4,  // Slower speed, used for basic storage or something like this
    CLASS_6,  // Medium speed, good for normal recording
    CLASS_10  // Fast speed, best for HD recording and high performance and so
}
